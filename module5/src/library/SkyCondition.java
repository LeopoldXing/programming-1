/**
 * An enumeration that is used to specify sky conditions for a weather forecast.
 *
 * @author Luping Xing [lxing2@rrc.ca]
 * @version 1.0.0
 */
public enum SkyCondition {
    /**
     * Represents a sunny sky condition.
     */
    SUNNY,

    /**
     * Represents a snowy sky condition.
     */
    SNOWY,

    /**
     * Represents a cloudy sky condition.
     */
    CLOUDY,

    /**
     * Represents a rainy sky condition.
     */
    RAINY
}
